const int MAXN = 110;

int a[MAXN][MAXN];

int main()
{
    cin.sync_with_stdio(false);
    int n;
    cin >> n;
    for (int i = 0; i < n; i++)
        for (int j = 0; j < n; j++)
            cin >> a[i][j];

    for (int k = 0; k < n; k++)
        for (int i = 0; i < n; i++)
            for (int j = 0; j < n; j++)
                a[i][j] = min(a[i][j], a[i][k] + a[k][j]);

    for (int i = 0; i < n; i++)
        for (int j = 0; j < n; j++)
            cout << a[i][j] << " \n"[j == n - 1];
    return 0;
}
